    private void EDayLeft() {
        File file = new File("Data/event.csv");
        if (!file.exists()) {
            updateDayLeftLabels(0, 0, 0, 0, 0, 0);
            updateTopicLabels("TOPIC", "TOPIC", "TOPIC", "TOPIC", "TOPIC", "TOPIC");
            updateDateLabels("DD/MM/YYYY", "DD/MM/YYYY", "DD/MM/YYYY", "DD/MM/YYYY", "DD/MM/YYYY", "DD/MM/YYYY");
            return;
        }
        
        class EventData {
            long daysLeft;
            String topic;
            String date;
            EventData(long daysLeft, String topic, String date) {
                      this.daysLeft = daysLeft;
                      this.topic = topic;
                      this.date = date;
            }
        }
        
         List<EventData> eventList = new ArrayList<>();

        //List<Map.Entry<Long, String>> dayTopicList = new ArrayList<>();
        //List<Map<String, Object>> eventList = new ArrayList<>();

        try (BufferedReader br = new BufferedReader(new FileReader(file))) {
             String line;
             LocalDate today = LocalDate.now();
             DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");

            while ((line = br.readLine()) != null) {
                    String[] parts = line.split(",");
                    if (parts.length >= 2) {
                    String topic = parts[0].trim();
                    String dateStr = parts[1].trim();
                    String[] dateRange = dateStr.split("-");
                    String startDateStr = dateRange[0].trim();
                    
                    try {
                         LocalDate eventDate = LocalDate.parse(startDateStr, formatter);
                         long daysLeft = ChronoUnit.DAYS.between(today, eventDate);

                        if (daysLeft >= 0) {
                            eventList.add(new EventData(daysLeft, topic, startDateStr));
                        } 
                    } catch (Exception e) {
                        System.out.println("Invalid date format in line: " + line);
                    }
                }
            }
        } catch (IOException e) {
                 e.printStackTrace();
        }

        eventList.sort(Comparator.comparingLong(e -> e.daysLeft));
        
        long[] days = new long[6];
        String[] topics = new String[6];
        String[] dates = new String[6];
        for (int i = 0; i < 6; i++) {
             if (i < eventList.size()) {
                 days[i] = eventList.get(i).daysLeft;
                 topics[i] = eventList.get(i).topic;
                 dates[i] = eventList.get(i).date;
             } else {
                 days[i] = 0;
                 topics[i] = "TOPIC";
                 dates[i] = "DD/MM/YYYY";
             }
        }

        SwingUtilities.invokeLater(() -> {
        updateDayLeftLabels(days[0], days[1], days[2], days[3], days[4], days[5]);
        updateTopicLabels(topics[0], topics[1], topics[2], topics[3], topics[4], topics[5]);
        updateDateLabels(dates[0], dates[1], dates[2], dates[3], dates[4], dates[5]);
        });
    }
    
