    private void Notification() {
        ScheduledExecutorService scheduler = Executors.newSingleThreadScheduledExecutor();
        scheduler.scheduleAtFixedRate(() -> {
        int count = 0;
        List<String> notifyEvents = new ArrayList<>();

    try (BufferedReader br = new BufferedReader(new FileReader("Data/event.csv"))) {
        String line;
        DateTimeFormatter dateFmt = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        DateTimeFormatter timeFmt = DateTimeFormatter.ofPattern("HH:mm");

        while ((line = br.readLine()) != null) {
            String[] parts = line.split(",");
            if (parts.length < 6) continue;

            String topic = parts[0].trim();
            String[] dateRange = parts[1].split("-");
            String[] timeRange = parts[2].split("-");

            LocalDate date = LocalDate.parse(dateRange[0].trim(), dateFmt);
            LocalTime startTime = LocalTime.parse(timeRange[0].trim(), timeFmt);
            LocalDateTime eventStart = LocalDateTime.of(date, startTime);

            int alarmValue = Integer.parseInt(parts[4].trim());
            String alarmUnit = parts[5].trim();

            LocalDateTime alarmTime = switch (alarmUnit) {
                case "Minute" -> eventStart.minusMinutes(alarmValue);
                case "Hour" -> eventStart.minusHours(alarmValue);
                case "Day" -> eventStart.minusDays(alarmValue);
                default -> eventStart;
            };

            LocalDateTime now = LocalDateTime.now();

            if (now.isAfter(alarmTime) && now.isBefore(eventStart)) {
                count++;
                notifyEvents.add(topic + " (" + eventStart.format(DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm")) + ")");
            }
        }

    } catch (IOException e) {
        e.printStackTrace();
    }
    
    final int finalCount = count;

    SwingUtilities.invokeLater(() -> {
        System.out.println("Count to show: " + finalCount);
        //Noti.setText(String.format("%d", finalCount));
        //home.setNotifyList(notifyEvents);
    });

    }, 0, 1, TimeUnit.MINUTES);
    }

    
